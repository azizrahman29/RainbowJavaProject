package wrapperClass;

public class AutoboxingCase2 {

	public static void main(String[] args) {
		// automatically converting data type 
		
		// Wrapper --> primitive 
		
		Integer iNumber = 5; // object  --> Assigning int to Integer: Auto-boxing 
		
		System.out.println("Automatically converted: " + iNumber);
		
		
		// Long 
		Long lNumber = 502L; 
		System.out.println(lNumber);
	}

}
