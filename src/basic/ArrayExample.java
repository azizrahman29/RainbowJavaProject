package basic;

public class ArrayExample {

	public static void main(String[] args) {
		// 
		
		System.out.println(" Welcome to java !!");

		String[] cars = new String[4];

		
		cars[0] = "Mustang";
		cars[1] = "Corvette";
		cars[2] = "Tesla";
		cars[3] = "BMW";
		
		for (int i =0 ; i<cars.length; i++) {
			
		
		System.out.println(cars[i]);
	}
}
}