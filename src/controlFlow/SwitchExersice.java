package controlFlow;

public class SwitchExersice {

	public static void main(String[] args) {
		// Switch Exercise
		
		int age;
		age=2;
		
		switch (age) {
		
		case 1:
				System.out.println("You can smill");
			break;
			
			case 2:
				System.out.println(" You can croul");
		break;
		
		case 3: 
			System.out.println("You can talk");
		break;
		case 4:
			System.out.println("You can walk");
		break;
		
		default:
			System.out.println("I don't know How old you are!");
		
		}

	}

}
