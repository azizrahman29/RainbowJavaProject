package ArrayExample;

public class CallingDisplay {

	public static void main(String[] args) {
		// calling
		displayText();
		waze();

	}

	// creating static method in java / static means not dynamic 
	static void displayText() {

		// method body
		System.out.println("I am learning the basic structure of Method!");
	}

	// create a method to open the Waze Application

	static void waze() {

		// method body
		System.out.println("Opening the Waze Application");

	}

}
