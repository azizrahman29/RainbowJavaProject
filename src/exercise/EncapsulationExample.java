package exercise;

public class EncapsulationExample {

	
	private int ssn;
	private String name;
	private int age;
	private int pN;
	
	
	
	public int getSNN() {
		return ssn;
	}
	
	public void setSSN(int newValue) {
		ssn = newValue;
	}
	
	public String getName () {
		return name;
		
	}
	
	public void setName(String newName) {
		name = newName;
	}
	
	public int getAge() {
		return age;
		
	}
	
	public void setAge(int newAge) {
		age = newAge;
		
	}
	
	public int getpN() {
		return pN;
		
	}
	
	public void setPN(int newPN) {
		pN = newPN;
	}
	
}


